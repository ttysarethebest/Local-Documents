#include <unistd.h>

void	ft_putchar(char a)
{
	write(1, &a, 1);
}

int		main(void)
{
	int i;

	i = 0;
	while (i < 26)
	{
		ft_putchar(97 + i);
		i++;
		ft_putchar(65 + i);
		i++;
	}
	return (0);
}	


#include <stdio.h>

int		match(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		i++;
		if (*s1 == *s2)
			return (1);
		if (s1[i] == '*' || s2[i] != '\0')
			return (1);
		if (s2[i] == '*' || s1[i] != '\0')
			return (1);
		else
			return (0);
	}
	return (0);
}

int		main(void)
{
	printf("%d", match("l*asda", "hello"));
	return(0);
}

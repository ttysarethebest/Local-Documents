int		ft_atoi(int a, char **argv)
{
	int i;
	int j;
	int k;
	int n;

	n = 0;
	j = 0;
	k = 1;
	i = 0;
	if (argv[a][0] == 45)
		n = 1;
	while (argv[a][i] != '\0')
		i++;
	i--;
	while (i >= n)
	{
		j = j + ((argv[a][i] - 48) * k);
		i--;
		k = k * 10;
	}
	if (argv[a][0] == 45)
		j = j * -1;
	return (j);
}
